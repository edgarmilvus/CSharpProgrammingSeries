using System;

public class Program
{
    public static void Main()
    {
        // Test cases calling the method with different arguments
        EvaluateGrade("Alice", 95);
        EvaluateGrade("Bob", 82);
        EvaluateGrade("Charlie", 55);
    }

    // Method definition: accepts string and int, returns void
    public static void EvaluateGrade(string name, int score)
    {
        string grade;

        // Determine grade using if-else if-else structure
        if (score >= 90)
        {
            grade = "A";
        }
        else if (score >= 80)
        {
            grade = "B";
        }
        else if (score >= 70)
        {
            grade = "C";
        }
        else if (score >= 60)
        {
            grade = "D";
        }
        else
        {
            grade = "F";
        }

        // Print the result directly since the method returns void
        Console.WriteLine($"Student: {name}, Score: {score}, Grade: {grade}");
    }
}
