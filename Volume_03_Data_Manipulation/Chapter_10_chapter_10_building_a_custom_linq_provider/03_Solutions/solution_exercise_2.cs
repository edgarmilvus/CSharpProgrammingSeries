public static class VectorMath
{
    // Assumes inputs are normalized unit vectors
    public static float CosineSimilarity(float[] v1, float[] v2)
    {
        if (v1 == null || v2 == null || v1.Length != v2.Length)
            return 0f; // Or throw exception based on strictness

        // Dot Product: Sum(a_i * b_i)
        // Zip pairs elements: (v1[0], v2[0]), (v1[1], v2[1]), ...
        float dotProduct = v1.Zip(v2, (a, b) => a * b).Sum();

        return dotProduct;
    }
}
