// BAD: Closure capture issue
var queries = new List<Func<IEnumerable<double>>>();
for (int i = 0; i < 3; i++)
{
    // 'i' is captured. By the time the query runs, 'i' will be 3.
    queries.Add(() => rawData.Select(p => p.Price + i));
}
// All queries will behave as if i was 3.
